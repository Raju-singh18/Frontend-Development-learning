

const date = new Date("2025-10-02T15:45:30");

console.log("Year:", date.getFullYear());
console.log("Month:", date.getMonth());    // 0 = Jan
console.log("Day of Month:", date.getDate());
console.log("Day of Week:", date.getDay()); // 0 = Sun
console.log("Hours:", date.getHours());
console.log("Minutes:", date.getMinutes());
console.log("Seconds:", date.getSeconds());

/* output
Year: 2025
Month: 9
Day of Month: 2
Day of Week: 4
Hours: 15
Minutes: 45
Seconds: 30
*/