
const d = new Date();

console.log(d.toDateString());        // Simple date
console.log(d.toTimeString());        // Full time
console.log(d.toLocaleDateString());  // Localized date
console.log(d.toLocaleTimeString());  // Localized time

/* output
Thu Oct 02 2025
23:58:05 GMT+0530 (India Standard Time)
10/2/2025
11:58:05 PM
*/
