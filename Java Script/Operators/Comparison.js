
let a = 10;
let b = "10";

console.log(a == b);   // true  (loose equality)
console.log(a === b);  // false (strict equality)
console.log(a != b);   // false
console.log(a !== b);  // true
console.log(a > 5);    // true
console.log(a < 5);    // false
console.log(a >= 10);  // true
console.log(a <= 9);   // false
